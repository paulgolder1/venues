<b>The Venues Project</b> is a free website dedicated to helping music lovers in the UK find new venues and bands.<br /><br />

Our database holds details of over 17,000 artists (groups and performers), over 9,000 venues and over 44,000 events across over 500 genres.<br /><br />

<b>How do I use it?</b><br /><br />

You can do a search for nearby venues by clicking <a href="near_me.php">WHAT'S NEAR ME?</a>.  When you enter your postcode you can then find nearby venues and then click further to see which artists have played there, and which genres play at these venues.<br /><br />

You can also use our <a href="search.php">SEARCH</a> function to find:<br /><br />
<ul>
<li>Venues - enter part or all of the name or town
<li>Artists - enter part or all of the name
<li>Genre - find different genres with similar names, eg "rock"
</ul>
Check out our <a href="stats.php">STATS</a> to see the latest number of venues, artists and genres in our database.<br /><br />
Accessability: click the FONT SIZE buttons at the top to increase or decrease the font size to make viewing more comfortable.<br /><br />

